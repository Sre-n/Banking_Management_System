CREATE DATABASE IF NOT EXISTS bankdb;
USE bank;

-- --------------------------------------------------------

--
-- Table structure for table account
--

CREATE TABLE bank (
  accno varchar(10) NOT NULL,
  name varchar(20) NOT NULL,
  mobile varchar(10),
  email varchar(20),
  address varchar(20),
  city varchar(10),
  country varchar(20),
  balance int,
  PRIMARY KEY (accno)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;
